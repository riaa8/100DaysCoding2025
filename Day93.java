public class Day93 {
    public static void main(String[] args) {
        //Method ceil(), floor() dan round()

        double r = 3.53;
        
        System.out.println("ceil 3.53 = " + Math.ceil(r));
        System.out.println("floor 3.53 = " + Math.floor(r));
        System.out.println("round 3.53 = " + Math.round(r));
    }
    
}
