package Package;
public class Day16 {
    public static void main(String[] args) {
        int ang1 = 5;
        int ang2 = 25;
        
        System.out.println("===OPERATOR ARITMATIKA PERKALIAN DAN PEMBAGIAN===");
        System.out.println("5 x 25 = " +(ang1 * ang2));
        System.out.println("5 x 5 = " +(ang1 * ang1));
        System.out.println("25 / 5 = " +(ang2 / ang1));
        System.out.println("25 / 25 = " +(ang2 / ang2));
    }
    
}
