package DayOfCoding;

public class Day6 {

    public static void main(String[] args) {
        double angka1 = 3.123467842254; //double Menyimpan angka pecahan. Cukup untuk menyimpan 15 hingga 16 digit desimal.
        float angka2 = 2.34567f; //float Menyimpan angka pecahan. Cukup untuk menyimpan 6 hingga 7 digit desimal.


        System.out.println("Double : " + angka1); //untuk mencetak double
        System.out.println("Float : " + angka2); //untuk mencetak float
    }

}
