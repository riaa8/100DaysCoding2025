package DayOfCoding;
public class Day9 {
    public static void main(String[] args) {
        String N;
        int U;
        
        N = "Ria Ramadani";
        U = 20; // deklarasi + inisialisasi
        System.out.println("Nama : " + N);
        System.out.println("Umur: " + U);
        
        N = "Ria";
        U = 21; // update nilai
        System.out.println("Nama setelah diupdate: " + N);
        System.out.println("Umur setelah di update : " + U);

    }
}


